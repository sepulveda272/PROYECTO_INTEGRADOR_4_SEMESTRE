    private void botoneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoneditarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un funcionario para editar.");
            return;
        }

        int idFuncionario = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
        long numeroIdentificacion = Long.parseLong(jTable1.getValueAt(fila, 1).toString());
        String tipoIdentificacion = jTable1.getValueAt(fila, 2).toString();
        String primerNombre = jTable1.getValueAt(fila, 3).toString();
        String segundoNombre = jTable1.getValueAt(fila, 4) != null ? jTable1.getValueAt(fila, 4).toString() : "";
        String primerApellido = jTable1.getValueAt(fila, 5).toString();
        String segundoApellido = jTable1.getValueAt(fila, 6) != null ? jTable1.getValueAt(fila, 6).toString() : "";
        long celular = Long.parseLong(jTable1.getValueAt(fila, 7).toString());
        String correo = jTable1.getValueAt(fila, 8).toString();
        String estado = jTable1.getValueAt(fila, 9).toString();

        ActualizarFuncionarioICA actualizar = new ActualizarFuncionarioICA(
            idFuncionario, numeroIdentificacion, tipoIdentificacion,
            primerNombre, segundoNombre, primerApellido, segundoApellido, celular, correo,estado, this::cargarFuncionarios
        );
        actualizar.setVisible(true);
    }//GEN-LAST:event_botoneditarActionPerformed

    private void botoneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoneliminarActionPerformed
        int viewRow = jTable1.getSelectedRow();
        if (viewRow == -1) { JOptionPane.showMessageDialog(this, "Seleccione un funcionario para eliminar."); return; }
        int row = jTable1.convertRowIndexToModel(viewRow);
        int idFuncionario = ((Number) jTable1.getModel().getValueAt(row, 0)).intValue();

        int res = JOptionPane.showConfirmDialog(this, "¿Está seguro de inactivar este funcionario?",
                "Confirmar", JOptionPane.YES_NO_OPTION);
        if (res == JOptionPane.YES_OPTION) {
            if (funcionarioICAController.desactivarFuncionarioSeguro(idFuncionario)) {
                JOptionPane.showMessageDialog(this, "✅ Funcionario inactivado.");
                cargarFuncionarios();
            } else {
                JOptionPane.showMessageDialog(this, "❌ No se pudo inactivar el funcionario. Verifique dependencias (OBSERVACIONES).");
            }
        }
    }//GEN-LAST:event_botoneliminarActionPerformed

