     private void cargarFuncionarios() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Funcionario");
        modelo.addColumn("Número Identificación");
        modelo.addColumn("Tipo Identificación");
        modelo.addColumn("Primer Nombre");
        modelo.addColumn("Segundo Nombre");
        modelo.addColumn("Primer Apellido");
        modelo.addColumn("Segundo Apellido");
        modelo.addColumn("Celular");
        modelo.addColumn("Correo");
        modelo.addColumn("Estado");

        List<FuncionarioICA> funcionarios = funcionarioICAController.obtenerFuncionarios();
        for (FuncionarioICA p : funcionarios) {
            Object[] fila = {
                p.getId_funcionario(),
                p.getNumero_identificacion(),
                p.getTipo_identificacion(),
                p.getPrimer_nombre(),
                p.getSegundo_nombre(),
                p.getPrimer_apellido(),
                p.getSegundo_apellido(),
                p.getCelular(),
                p.getCorreo(),
                p.getEstado()
            };
            modelo.addRow(fila);
        }
        jTable1.setModel(modelo);
    }

