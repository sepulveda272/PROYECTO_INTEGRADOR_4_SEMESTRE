    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String fecha = txtFecha.getText().trim();
            String texto = txtObsercaiocnes.getText().trim();

            if (!validarFechaCampo(txtFecha, "Fecha de la observación")) return;

            if (texto.isEmpty()) {
                JOptionPane.showMessageDialog(this, "El texto de la observación es obligatorio.");
                return;
            }

            int idxInsp = cboInspeccion.getSelectedIndex();
            int idxFunc = cboFuncionario.getSelectedIndex();
            if (idxInsp < 0 || idxFunc < 0) {
                JOptionPane.showMessageDialog(this, "Seleccione inspección y funcionario.");
                return;
            }

            int idInspeccion = inspeccionIds.get(idxInsp);
            int idFuncionario = funcionarioIds.get(idxFunc);

            String error = observacionesController.crear(
                    fecha,
                    texto,
                    idInspeccion,
                    idFuncionario
            );

            if (error == null) {
                JOptionPane.showMessageDialog(this, "✅ Observación registrada.");
                if (onObservacionGuardada != null) onObservacionGuardada.run();
                dispose();
            } else {
                JOptionPane.showMessageDialog(this, error, "Error al crear", JOptionPane.WARNING_MESSAGE);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, "Ocurrió un error al guardar la observación.");
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

