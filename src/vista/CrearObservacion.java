    private boolean validarFechaCampo(JTextField campo, String etiqueta) {
        String s = campo.getText().trim();
        if (s.isEmpty()) {
            JOptionPane.showMessageDialog(this,
                "La " + etiqueta + " es obligatoria.",
                "Fecha inválida",
                JOptionPane.WARNING_MESSAGE
            );
            campo.requestFocus();
            return false;
        }
        try {
            LocalDate.parse(s, FMT_YMD);
            return true;
        } catch (DateTimeParseException ex) {
            JOptionPane.showMessageDialog(this,
                "Formato inválido en " + etiqueta + ": \"" + s + "\".\n" +
                "Usa yyyy-MM-dd (ej. 2025-11-09).",
                "Fecha inválida",
                JOptionPane.WARNING_MESSAGE
            );
            campo.requestFocus();
            return false;
        }
    }
    
    private void cargarCombos() {
        cboInspeccion.removeAllItems();
        cboFuncionario.removeAllItems();
        inspeccionIds.clear();
        funcionarioIds.clear();

        // 1) Inspecciones para escoger a cuál pertenece la observación
        InspeccionFitosanitariaController inspCtrl = new InspeccionFitosanitariaController();
        for (InspeccionFitosanitaria insp : inspCtrl.listarConTecnicoEIncidencia()) {
            // texto amigable: "ID 10 - Lote 3 - 2025-11-10"
            String etiqueta = "ID " + insp.getId_inspeccion()
                            + " - Lote " + insp.getNumero_lote()
                            + " - " + insp.getFecha_inspeccion();
            cboInspeccion.addItem(etiqueta);
            inspeccionIds.add(insp.getId_inspeccion());
        }

        // 2) Funcionarios ICA
        FuncionarioICAController funcCtrl = new FuncionarioICAController();
        for (FuncionarioICA f : funcCtrl.obtenerFuncionariosActivos()) {
            String nombreCompleto = f.getPrimer_nombre() + " " + f.getPrimer_apellido();
            cboFuncionario.addItem(nombreCompleto);
            funcionarioIds.add(f.getId_funcionario());
        }
    }

