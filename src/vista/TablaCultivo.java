    private void botoneditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoneditarActionPerformed
        int fila = jTable1.getSelectedRow();

        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cultivo para editar.");
            return;
        }

        int idCultivo = Integer.parseInt(jTable1.getValueAt(fila, 0).toString());
        String nombreEspecie  = jTable1.getValueAt(fila, 1).toString();
        String variedad  = jTable1.getValueAt(fila, 2).toString();
        String descripcion  = jTable1.getValueAt(fila, 3) != null ? jTable1.getValueAt(fila, 3).toString() : "";

        ActualizarCultivo actualizar = new ActualizarCultivo(
            idCultivo, nombreEspecie, variedad, descripcion, this::cargarCultivos
        );
        actualizar.setVisible(true);
    }//GEN-LAST:event_botoneditarActionPerformed

    private void botoneliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botoneliminarActionPerformed
        int viewRow = jTable1.getSelectedRow();
        if (viewRow == -1) {
            JOptionPane.showMessageDialog(this, "Seleccione un cultivo para eliminar.");
            return;
        }
        
        int row = jTable1.convertRowIndexToModel(viewRow);
        int idCultivo  = Integer.parseInt(jTable1.getModel().getValueAt(row, 0).toString());

        int res = JOptionPane.showConfirmDialog(
            this, "¿Está seguro de eliminar este cultivo?", "Confirmar", JOptionPane.YES_NO_OPTION
        );
        if (res == JOptionPane.YES_OPTION) {
            if (cultivoController.eliminarCultivo(idCultivo )) {
                JOptionPane.showMessageDialog(this, "✅ Cultivo eliminado.");
                cargarCultivos();
            } else {
                JOptionPane.showMessageDialog(this, "❌ No se pudo eliminar. Verifique dependencias (LOTE).");
            }
        }
    }//GEN-LAST:event_botoneliminarActionPerformed

