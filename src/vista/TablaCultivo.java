     private void cargarCultivos() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Cultivo");
        modelo.addColumn("Nombre especie");
        modelo.addColumn("Variedad");
        modelo.addColumn("Descripción");

        List<Cultivo> cultivos = cultivoController.listarCultivos();
        for (Cultivo c : cultivos) {
            Object[] fila = {
                c.getId_cultivo(),
                c.getNombre_especie(),
                c.getVariedad(),
                c.getDescripcion()
            };
            modelo.addRow(fila);
        }

        jTable1.setModel(modelo);
    }

